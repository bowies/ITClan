package spring.model.offer;

public class OfferDTO {
	private int 	offerNum			;           
	private String  title               ;
	private String  qualification       ;
	private String	offerDate           ;
	private String	companyName         ;
	private String	companyID           ;
	private String	detail              ;
	private String	employmentSector    ;
	private String	task                ;
	private String	workingArea         ;
	private String	recruitment         ;
	private String	salary			    ;
	private String viewcnt;
	
	
	
	public String getViewcnt() {
		return viewcnt;
	}
	public void setViewcnt(String viewcnt) {
		this.viewcnt = viewcnt;
	}
	public int getOfferNum() {
		return offerNum;
	}
	public void setOfferNum(int offerNum) {
		this.offerNum = offerNum;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getQualification() {
		return qualification;
	}
	public void setQualification(String qualification) {
		this.qualification = qualification;
	}
	public String getOfferDate() {
		return offerDate;
	}
	public void setOfferDate(String offerDate) {
		this.offerDate = offerDate;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getCompanyID() {
		return companyID;
	}
	public void setCompanyID(String companyID) {
		this.companyID = companyID;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	
	public String getEmploymentSector() {
		return employmentSector;
	}
	public void setEmploymentSector(String employmentSector) {
		this.employmentSector = employmentSector;
	}
	public String getTask() {
		return task;
	}
	public void setTask(String task) {
		this.task = task;
	}
	public String getWorkingArea() {
		return workingArea;
	}
	public void setWorkingArea(String workingArea) {
		this.workingArea = workingArea;
	}
	public String getRecruitment() {
		return recruitment;
	}
	public void setRecruitment(String recruitment) {
		this.recruitment = recruitment;
	}
	public String getSalary() {
		return salary;
	}
	public void setSalary(String salary) {
		this.salary = salary;
	}
	

			
}